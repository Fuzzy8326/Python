# 2-4. Name Cases
# Store the name "harry potter" in a variable
name = "harry potter"

# Convert the name to all lowercase letters and print it
print(name.lower())    # Output: "harry potter"

# Convert the name to all uppercase letters and print it
print(name.upper())    # Output: "HARRY POTTER"

# Convert the name to title case (first letter of each word capitalized) and print it
print(name.title())    # Output: "Harry Potter"
